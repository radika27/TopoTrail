// ---- Helper: konversi data demo ke GeoJSON ----
function loadDemo(key) {
  const d = DEMOS[key];
  const gj = {
    type: 'FeatureCollection',
    features: [
      // LineString jalur utama
      {
        type: 'Feature',
        geometry: { type: 'LineString', coordinates: d.line },
        properties: { name: d.name }
      },
      // Point waypoints
      ...d.wpts.map(w => ({
        type: 'Feature',
        geometry: { type: 'Point', coordinates: w.c },
        properties: { name: w.name, elevasi: w.c[2] + ' m' }
      }))
    ]
  };
  addLayer(gj, d.name);
  map.setView(d.center, 13);
}
